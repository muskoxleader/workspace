alias lls='ls -lGFhSrA'		# List almost all by size, biggest last
alias llt='ls -lGFhtrA'		# List almost all by date, most recent last
alias llc='ls -lGFhtcrA'	# List almost all by change time, most recent last
alias llu='ls -lGFhturA'	# List almost all by access time, most recent last
